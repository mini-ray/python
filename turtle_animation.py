def clear(t):
	time.sleep(.5)
	t.goto(-200,0)
	t.color("white")
	t.pendown()
	t.begin_fill()
	t.right(90)
	t.forward(400)
	t.left(90)
	t.forward(385)
	t.left(90)
	t.forward(400)
	t.left(90)
	t.forward(385)
	t.end_fill()

def main():
	try:
		turtle.tracer(2,0)
		turtle.TurtleScreen._RUNNING = True
		t = turtle.Turtle()
		screen = turtle.Screen()
		bob = 0
		movie(t)
		clear(t)
		while (bob == 0):
			legs1(t)
			clear(t)
			legs2(t)
			clear(t)
		screen.exitonclick()
	finally:
		turtle.Terminator()

if __name__ == '__main__':
    main()
