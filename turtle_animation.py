def arcL(t,x,y,l,tn,h):
	#x, y, l (length) tn(turn value)
	t.down()
	t.seth(h)
	for i in range (1,10):
			t.forward(l)
			t.lt(tn)
			
def circle(t):
	print("circle")
	t.down()
	t.color("#ff0000")
	for i in range (0,10):
		t.forward(10)
		t.rt(36)

